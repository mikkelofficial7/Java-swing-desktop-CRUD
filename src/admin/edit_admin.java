    private void btn_insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_insertActionPerformed
        try 
        { 
            connect();
            if(jTextField1.getText().length()>0 && jTextField2.getText().length()>0 && jTextField3.getText().length()>0 && jTextField4.getText().length()>0)
            {
                String id = null;
                String query = "SELECT id FROM data_barang where id = '"+jTextField1.getText()+"'";
                rs = st.executeQuery(query);
                while(rs.next())
                {
                    id = rs.getString("id");
                    System.out.println(id);
                }
                if(jTextField1.getText().equals(id))
                {
                    JOptionPane.showMessageDialog(null, "Id is already registered");
                }
                else
                {
                    try 
                    {
                        Integer.parseInt(jTextField3.getText()); //check whether price and stock in number format
                        Integer.parseInt(jTextField4.getText());
                        st.executeUpdate("INSERT INTO data_barang(id,nama_barang,harga,stock,foto) VALUES ('"+ jTextField1.getText()+"', '"+ jTextField2.getText()+"', '"+ jTextField3.getText()+"', '"+ jTextField4.getText()+"','foto/image.jpg')");
                        JOptionPane.showMessageDialog(null, "Successfully Inserted");

                        //Load or refresh table
                        loadTableInventory();
                        con.close();
                    }
                    catch (NumberFormatException e)
                    {
                        JOptionPane.showMessageDialog(null, "Price or Stock isn't number format"); 
                    }
                }
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Fields cannot be empty"); 
            }
        }
        catch (Exception e) 
        { 
            System.err.println("Got an exception! Cannot be inserted"); 
            System.err.println(e.getMessage()); 
        } 
    }//GEN-LAST:event_btn_insertActionPerformed
  
