    private void loadTableInventory()
    {
        Vector col = new Vector();
        Vector dat= new Vector();
        try 
        {
            rs=st.executeQuery("select * from data_barang order by id");
            col.add("Id Product");
            col.add("Product Name");// create contact default colum names and sore it
            col.add("Price");//This texts will display in the header of the table
            col.add("Stock"); //getting allthe information from the table
            col.add("Image Sample");

            dat.clear(); //clear the object before use it

            while(rs.next())
            {// if record source avilable
                Vector v =new Vector();
                v.add(rs.getString("id").trim());
                v.add(rs.getString("nama_barang").trim());
                v.add(rs.getString("harga").trim());
                v.add(rs.getString("stock").trim());// getting contacts from database and store in object
                v.add(rs.getString("foto").trim());
                
                dat.add(v);
            }
            jTable1.setModel(new DefaultTableModel(dat, col)); //finnaly set all the values to the database
        }
        catch(Exception ex)
        {
            System.out.println(ex);       
        }
    }
